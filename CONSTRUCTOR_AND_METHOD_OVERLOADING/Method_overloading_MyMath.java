package METHODS_CONSTRUCTOR_OVERLOADING;

public class Method_overloading_MyMath {
    public int add(int a,int b)
    {
        return a+b;
    }
    public int add(int a,int b,int c)
    {
        return a+b+c;
    }
    public String add(String a,String b)
    {
        return a+b;
    }
}
